/* Q: Use a for...of loop to iterate over an array of fruits:
const fruits = ["apple", "banana", "cherry"]; Log each fruit to the console. */

// for...of Loop: The for...of loop is a simple way to go through each item in an array or other iterable (like a string, array, or object).

const fruits = ["apple", "banana", "cherry"];
for (const fruit of fruits) {
    console.log(fruit);
}

